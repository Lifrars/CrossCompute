package Clases;

import java.io.InputStream;

public class Producto {
    int id;
    String nombres;    
    InputStream foto;
    String descripcion;
    double precio;
    int stock;
    String categoria;
    String estado;

    public Producto() {
    }

    public Producto(int id, String nombres, InputStream imagen, InputStream foto, String descripcion, double precio, int stock, String categoria, String estado) {
        this.id = id;
        this.nombres = nombres;
        this.foto = foto;
        this.descripcion = descripcion;
        this.precio = precio;
        this.stock = stock;
        this.categoria = categoria;
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public InputStream getFoto() {
        return foto;
    }

    public void setFoto(InputStream foto) {
        this.foto = foto;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    @Override
    public String toString() {
        return "Producto{" + "id=" + id + ", nombres=" + nombres + ", foto=" + foto + ", descripcion=" + descripcion + ", precio=" + precio + ", stock=" + stock + '}';
    }

    
}
